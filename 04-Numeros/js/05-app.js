let puntaje = 5;

puntaje++; // Si pongo esto retorna 5 (pero se sumo 1)
++puntaje; // Si pongo esto retorna 6 ( y el valor es 6)

// Lo mismo con el --

puntaje--; // Si pongo esto retorna 5 (pero se resto 1)
--puntaje; // Si pongo esto retorna 4 ( y el valor es 4)

puntaje += 3; // Si pongo esto retorna 8
puntaje -= 3; // Si pongo esto retorna 2

console.log(puntaje);